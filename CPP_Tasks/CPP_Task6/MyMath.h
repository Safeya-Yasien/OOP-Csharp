#include <iostream>
#include <cmath>

double sum(double a, double b)
{
    return (a + b);
}

double sub(double a, double b)
{
    return (a - b);
}

double mul(double a, double b)
{
    return (a * b);
}

double div(double a, double b)
{
    return (a / b);
}

double mod(double a, double b)
{
    return ((int)a % (int)b);
}

double Pow(double a, double b)
{
    return (pow(a, b));
}

