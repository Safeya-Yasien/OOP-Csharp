class MyMath
{
    public static int Sum(int x, int y)
    {
        return (x + y);
    }
    
    public static int Sub(int x, int y)
    {
        return (x - y);
    }
    public static int Mul(int x, int y)
    {
        return (x * y);
    }
    public static int Div(int x, int y)
    {
        return (x / y);
    }
    public static int Rem(int x, int y)
    {
        return (x % y);
    }
}
